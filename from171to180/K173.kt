package from171to180

import kotlin.math.exp
import kotlin.math.expm1
import kotlin.math.pow
import kotlin.math.sqrt

/*
지수함수
sqrt - square root 제곱근
pow - 리시버의 인수 제곱
exp - e의 인수 제곱
expm1 - e의 인수 제곱 -1
 */

fun main()
{
    println(sqrt(2.0))
    println(2.0.pow(0.5))
    println(exp(2.0))
    println(expm1(2.0))
}