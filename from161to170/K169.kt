package from161to170

import kotlin.math.*

/*
실수 값 올림, 내림, 반올림
 */

fun main(){
    println("${ceil(5.2)}")
    println("${ceil(5.6)}")
    println("${floor(5.2)}")
    println("${floor(5.6)}")
    println("${round(5.2)}")
    println("${round(5.6)}")
    println("${15.62.roundToInt()}")
    println("${52419.58.roundToLong()}")
}